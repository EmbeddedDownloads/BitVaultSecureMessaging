package com.app.securemessaging.bean;

/**
 * This is model class used to set contact data
 */

public class ContactsModel {

    private String mName;
    private String mPhone;
    private String mEmail;
    private String mReceiverAddress;
    private String mImage;

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmPhone() {
        return mPhone;
    }

    public void setmPhone(String mPhone) {
        this.mPhone = mPhone;
    }

    public String getmEmail() {
        return mEmail;
    }

    public void setmEmail(String mEmail) {
        this.mEmail = mEmail;
    }

    public String getmReceiverAddress() {
        return mReceiverAddress;
    }

    public void setmReceiverAddress(String mReceiverAddress) {
        this.mReceiverAddress = mReceiverAddress;
    }

    public String getmImage() {
        return mImage;
    }

    public void setmImage(String mImage) {
        this.mImage = mImage;
    }
    
}
